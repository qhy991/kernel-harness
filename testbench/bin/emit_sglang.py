#!/usr/bin/env python3
"""Emit SGLang-native test + benchmark files for a testbench task.

`evaluate.py` scores a candidate through the repo's own harness (`testbench/harness/`).
This tool makes the SAME kernel comparable inside SGLang's OWN harness: it writes a
`test_<name>.py` (pytest, `torch.testing.assert_close`) and a `bench_<name>.py`
(`triton.testing.perf_report` + `do_bench_cudagraph`, IS_CI-gated, with a speedup
line) that drop into `sgl-kernel/{tests,benchmark}/` and are auto-discovered by
SGLang's CI (pytest collection + the `bench_*.py` glob loop) with no registration.

Both generated files import the task's own `reference.py` (the real sglang kernel =
correctness oracle + baseline) and `solution.py` (the candidate) BY PATH and build
inputs via `reference.get_inputs`, so results line up with `evaluate.py` and the task
stays the single source of truth.

Usage:
    python bin/emit_sglang.py <task_dir> [--dest <sgl-kernel dir>] [--stdout]

Default dest = <SGLANG_DIR>/sgl-kernel (resolved via bin/config.py). Writes
tests/test_<name>.py and benchmark/bench_<name>.py under dest.
"""
import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from config import SGLANG_DIR


def _sweep_axes(task_dir: Path):
    """Return the list of per-shape axes dicts from workload.jsonl."""
    out = []
    for line in (task_dir / "workload.jsonl").read_text().splitlines():
        line = line.strip()
        if line:
            out.append(json.loads(line)["axes"])
    return out


def _const_axes(task_dir: Path):
    """Const axes (K/N/E/...) and scalars the reference needs but aren't swept."""
    d = json.loads((task_dir / "definition.json").read_text())
    consts = {}
    for name, spec in d.get("axes", {}).items():
        if spec.get("type") == "const":
            consts[name] = spec["value"]
    # task.json scalars (top_k, num_experts, H, V, ...) the recipe reads from axes
    meta = json.loads((task_dir / "task.json").read_text())
    for k in ("top_k", "num_experts", "topk", "H", "V", "K", "N", "E", "Bh"):
        if k in meta and k not in consts:
            consts[k] = meta[k]
    return consts


_TEST_TEMPLATE = '''\
# Auto-generated by kernel-harness/testbench/bin/emit_sglang.py for task "{name}".
# Correctness of the candidate solution.py against the real sglang kernel
# (reference.py), on the task's shape sweep and tolerance. Drop-in for
# sgl-kernel/tests/ — auto-collected by `pytest tests/`.
import importlib.util
import sys
from pathlib import Path

import pytest
import torch

TASK_DIR = Path(r"{task_dir}")
ATOL, RTOL, RATIO = {atol}, {rtol}, {ratio}
CONST_AXES = {const_axes!r}
SWEEP = {sweep!r}


def _load(path, modname):
    spec = importlib.util.spec_from_file_location(modname, path)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


_ref = _load(TASK_DIR / "reference.py", "task_reference")
_sol = _load(TASK_DIR / "solution.py", "task_solution")


def _as_list(o):
    return list(o) if isinstance(o, (tuple, list)) else [o]


def _seeded_inputs(mod, scalars, device, seed=1234):
    # Fixed seed => reference and candidate get bit-identical inputs WITHOUT cloning
    # (cloning fp8 scale tensors drops their TMA-aligned stride and breaks deep_gemm).
    torch.manual_seed(seed)
    return mod.get_inputs(scalars, device)


@pytest.mark.parametrize("axes", SWEEP)
def test_{name}(axes):
    if not torch.cuda.is_available():
        pytest.skip("needs CUDA")
    device = torch.device("cuda")
    scalars = dict(CONST_AXES); scalars.update(axes)
    ref_out = _as_list(_ref.run(**_seeded_inputs(_ref, scalars, device)))
    cand_out = _as_list(_sol.run(**_seeded_inputs(_ref, scalars, device)))
    assert len(ref_out) == len(cand_out)
    for r, c in zip(ref_out, cand_out):
        if r.is_floating_point():
            ok = (c.float() - r.float()).abs() <= (ATOL + RTOL * r.float().abs())
            assert ok.float().mean().item() >= RATIO, (
                f"match {{ok.float().mean().item():.4f}} < {{RATIO}} for axes={{axes}}")
        else:
            torch.testing.assert_close(c, r, rtol=0, atol=0)   # exact-index oracle


if __name__ == "__main__":
    sys.exit(pytest.main([__file__, "-v"]))
'''


_BENCH_TEMPLATE = '''\
# Auto-generated by kernel-harness/testbench/bin/emit_sglang.py for task "{name}".
# Benchmarks the candidate solution.py against the real sglang kernel (reference.py)
# across the task's shape sweep, in SGLang's own perf_report/do_bench_cudagraph
# format. Drop-in for sgl-kernel/benchmark/ — auto-run by the CI `bench_*.py` loop.
import importlib.util
from pathlib import Path

import torch
import triton
import triton.testing

from sglang.utils import is_in_ci

IS_CI = is_in_ci()
TASK_DIR = Path(r"{task_dir}")
CONST_AXES = {const_axes!r}
SWEEP = {sweep!r}


def _load(path, modname):
    spec = importlib.util.spec_from_file_location(modname, path)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


_ref = _load(TASK_DIR / "reference.py", "task_reference")
_sol = _load(TASK_DIR / "solution.py", "task_solution")

# CI: one tiny shape to stay under the 60s/file budget. Local: the full sweep.
_configs = [SWEEP[0]] if IS_CI else SWEEP
# Index into _configs is the x-axis (a dict isn't a valid Triton x_val); the body
# looks the axes back up. Keeps arbitrary multi-axis sweeps working.
_x_vals = list(range(len(_configs)))


@triton.testing.perf_report(
    triton.testing.Benchmark(
        x_names=["cfg_idx"],
        x_vals=_x_vals,
        line_arg="provider",
        line_vals=["sglang", "candidate", "speedup"],
        line_names=["sglang (us)", "candidate (us)", "speedup x"],
        styles=[("green", "-"), ("blue", "-"), ("red", "--")],
        ylabel="us  (speedup: x)",
        plot_name="{name}-performance",
        args={{}},
    )
)
def benchmark(cfg_idx, provider):
    device = torch.device("cuda")
    axes = _configs[cfg_idx]
    scalars = dict(CONST_AXES); scalars.update(axes)
    q = [0.5, 0.2, 0.8]
    if provider == "sglang":
        inp = _ref.get_inputs(scalars, device)
        ms, mn, mx = triton.testing.do_bench_cudagraph(lambda: _ref.run(**inp), quantiles=q)
        return 1000 * ms, 1000 * mx, 1000 * mn
    if provider == "candidate":
        inp = _sol.get_inputs(scalars, device) if hasattr(_sol, "get_inputs") \\
            else _ref.get_inputs(scalars, device)
        ms, mn, mx = triton.testing.do_bench_cudagraph(lambda: _sol.run(**inp), quantiles=q)
        return 1000 * ms, 1000 * mx, 1000 * mn
    # provider == "speedup": baseline_time / candidate_time (>1 = candidate faster)
    inp_r = _ref.get_inputs(scalars, device)
    t_ref, _, _ = triton.testing.do_bench_cudagraph(lambda: _ref.run(**inp_r), quantiles=q)
    inp_c = _sol.get_inputs(scalars, device) if hasattr(_sol, "get_inputs") \\
        else _ref.get_inputs(scalars, device)
    t_cand, _, _ = triton.testing.do_bench_cudagraph(lambda: _sol.run(**inp_c), quantiles=q)
    spd = t_ref / t_cand if t_cand > 0 else 0.0
    return spd, spd, spd


if __name__ == "__main__":
    benchmark.run(print_data=True)
'''


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("task_dir", type=Path)
    ap.add_argument("--dest", type=Path, default=Path(SGLANG_DIR) / "sgl-kernel")
    ap.add_argument("--stdout", action="store_true", help="print instead of writing files")
    args = ap.parse_args()

    task_dir = args.task_dir.resolve()
    meta = json.loads((task_dir / "task.json").read_text())
    name = meta["name"]
    tol = meta.get("tolerance", {"max_atol": 0.1, "max_rtol": 0.05, "required_matched_ratio": 0.98})
    sweep = _sweep_axes(task_dir)
    const_axes = _const_axes(task_dir)

    fmt = dict(name=name, task_dir=str(task_dir), sweep=sweep, const_axes=const_axes,
               atol=tol["max_atol"], rtol=tol["max_rtol"], ratio=tol["required_matched_ratio"])
    test_src = _TEST_TEMPLATE.format(**fmt)
    bench_src = _BENCH_TEMPLATE.format(**fmt)

    if args.stdout:
        print("# ===== tests/test_%s.py =====" % name)
        print(test_src)
        print("# ===== benchmark/bench_%s.py =====" % name)
        print(bench_src)
        return

    test_path = args.dest / "tests" / f"test_{name}.py"
    bench_path = args.dest / "benchmark" / f"bench_{name}.py"
    test_path.parent.mkdir(parents=True, exist_ok=True)
    bench_path.parent.mkdir(parents=True, exist_ok=True)
    test_path.write_text(test_src)
    bench_path.write_text(bench_src)
    print(f"wrote {test_path}")
    print(f"wrote {bench_path}")


if __name__ == "__main__":
    main()
